import cv2	as cv


################################################################################
#	functions							       #
################################################################################
def wait_for_ESC():
	ESC	= 27;
	while (True):
		k	= cv.waitKey(5) & 0xFF;
		if k == ESC:
			break;


################################################################################
#	run								       #
################################################################################
img	= cv.imread("penguin.jpg", cv.IMREAD_GRAYSCALE);
cv.imshow("img", img);
wait_for_ESC();


